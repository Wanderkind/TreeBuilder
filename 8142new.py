def Sw(l,s):
    
    r=[i for i in range(10)]
    m=0
    
    while 1:
        random.shuffle(r)
        L=[o[:] for o in l[:]]
        for i in range(8):
            for j in range(14):
                for k in range(10):
                    if L[i][j]==k:
                        L[i][j]=r[k]
                        break
        
        t=Score(L)
        if t>m:
            print('')
            for i in L:
                print(''.join([str(j) for j in i]))
            print(t)
            m=t
        if t>s:
            print('\a')
            return (L,t)

