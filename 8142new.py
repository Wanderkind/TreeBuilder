def PhaseRepair(l,t,s):
    
    lt=len(t)
    z=l[:]
    
    for ii in range(lt):
        i,j,k=t[ii]
        print(f'Scanning {ii+1}/{lt}... ({i+1},{j+1},{k})')
        ll=[q[:] for q in l]
        ll[i][j]=k
        z,maxx=Phase(ll)
        print(maxx)
        if maxx>s:
            return (z,maxx)
    
    return (z,maxx)

